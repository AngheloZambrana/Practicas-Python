# Definir una función inversa() que calcule la inversión de una cadena.
# Por ejemplo la cadena "estoy probando" debería devolver la cadena "odnaborp yotse"
def inversa(cadena):
    return cadena[::-1]

# Ejemplo de uso
cadena = ("palindromo")
cadena_invertida = inversa(cadena)
print(cadena_invertida)
